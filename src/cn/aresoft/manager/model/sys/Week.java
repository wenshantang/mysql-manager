package cn.aresoft.manager.model.sys;

public class Week {
	private String sunday = "0";
	private String monday = "0";
	private String tuesday = "0";
	private String wendsday = "0";
	private String thursday = "0";
	private String friday = "0";
	private String saturday = "0";

	public String getSunday() {
		return sunday;
	}

	public void setSunday(String sunday) {
		this.sunday = sunday;
	}

	public String getMonday() {
		return monday;
	}

	public void setMonday(String monday) {
		this.monday = monday;
	}

	public String getTuesday() {
		return tuesday;
	}

	public void setTuesday(String tuesday) {
		this.tuesday = tuesday;
	}

	public String getWendsday() {
		return wendsday;
	}

	public void setWendsday(String wendsday) {
		this.wendsday = wendsday;
	}

	public String getThursday() {
		return thursday;
	}

	public void setThursday(String thursday) {
		this.thursday = thursday;
	}

	public String getFriday() {
		return friday;
	}

	public void setFriday(String friday) {
		this.friday = friday;
	}

	public String getSaturday() {
		return saturday;
	}

	public void setSaturday(String saturday) {
		this.saturday = saturday;
	}

}
